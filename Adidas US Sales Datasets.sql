## Section 4: Sales Method & Channel Efficiency

-- Query 10:How does in-store sales compare with online and outlet sales?
select 
   Sales_Method,
    sum(Total_Sales) as total_sales
from adidas_sales
group by Sales_Method
order by  Total_Sales desc;

-- Query 11:Which sales method has the highest operating margin (%)?
select 
    Sales_Method,
    sum(Operating_Margin) as avg_Operating_Margin
from adidas_sales

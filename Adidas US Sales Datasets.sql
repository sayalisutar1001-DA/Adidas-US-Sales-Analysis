## Section 1: Executive KPIs
-- Query 1: What is the total revenue and total operating profit generated by Adidas?
select sum(Total_Sales) as Total_revenue,
	   sum(Operating_Profit)as total_Operating_Profit
       from adidas_sales;


-- Query 2:What is the total volume of units sold across all retailers?
select sum(Units_Sold) as Total_Units_Sold
from adidas_sales;


-- Query 3:What is the overall average operating margin (%) for the business?
select sum(Operating_Margin)as AVG_Operating_Margin
from adidas_sales;

## Section 2: Product Performance Analysis

-- Query 4:Which are the top 5 products contributing the highest revenue?
select Product,
    sum(Total_Sales) as Total_Revenue
from adidas_sales
group by Product
order by Total_Revenue desc
limit 5;

-- Query 5:Which product category has the lowest sales volume?
select Product,
    sum(Units_Sold) as Total_Units_Sold
from adidas_sales
group by Product
order by Total_Units_Sold asc
limit 1;


-- Query 6:What is the average price per unit for each product category?
select Product, avg(Price_Per_Unit) as Avg_Price_Per_Unit
from adidas_sales
group by Product;

